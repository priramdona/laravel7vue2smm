<?php

namespace App\Traits;

use Illuminate\Support\Str;

trait HasUuids
{
    protected static function bootHasUuids()
    {
        static::creating(function ($model) {
            if (empty($model->{$model->getKeyName()})) {
                $model->{$model->getKeyName()} = (string) Str::uuid();
            }
        });
    }
}